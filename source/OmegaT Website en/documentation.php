<h3>User manuals</h3>




<p>OmegaT is supplied with a comprehensive user manual. To call up the manual from within OmegaT, simply select Help > Users manual, or press F1.</p>

<p>An Instant Start Guide, which guides you through the main procedures and functions of OmegaT, is displayed in the OmegaT user interface by default.</p>

<p>You can also call up the user manual supplied with OmegaT without launching OmegaT itself. It can be found in the "docs" folder in your OmegaT program folder.</p>


